class ErrorMessage {
    private String name;
    private String message;
    private int code;
    private int status;

    public String getName() {
        return name;
    }

    public String getMessage() {
        return message;
    }

    public int getCode() {
        return code;
    }

    public int getStatus() {
        return status;
    }
}
